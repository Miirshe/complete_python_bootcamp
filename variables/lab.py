quotes = 'hi`miirshe'
print(f'{quotes.title()}')
print(f' {quotes.lower()}') # lower( ) converts  our string object into the lowercase
print(f'{quotes.upper()}') # upper( ) converts our string object into the uppercase
print(f'{len(quotes)}') # len() method takes one argument and return the length of that values
print(f'{quotes.count("s")}') # count() takes one argument that filtering on your objects if not founded return 0
print(f'{quotes.replace("hi","hello")}') # replace() takes two argument that allows you to change value within your object
print(f'{quotes.find("m")}') # find() takes one argument find values in you object value
